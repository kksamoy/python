# -*- coding: utf-8 -*-
# 数据类型 list(列表)
a = [121, '12123', '1']
print(a, type(a))


# 数据类型 dict(字典)
a = {'a': '1', 'b': 2}
print(a, type(a))


# 数据类型 tuple(元祖)
a = (1, 2, 3, 4)
print(a, type(a))


# 基本语法 if如果  else否则
if 1 > 2:
    print('正确')
else:
    print('错误')


# 基本语法 for循环   break结束循环 10为循环的次数
for i in range(10):
    print(i)
    # break


# 基本语法 while循环  break结束循环  1==1为循环的条件
while 1 == 1:
    print(1)
    break